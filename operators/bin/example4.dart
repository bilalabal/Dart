  print(age + 20);
  print(age - 20);
  print(age * 20);
  print(age / 20);
  print(age ~/ 20);
  print(age % 20);
  print(age == 20);
  print(age != 20);

  print(age > 20);
  print(age < 20);
  print(age >= 20);
  print(age <= 20);
  print(age & 20);
  print(age | 20);
  print(age ^ 20);

  print(age << 20);
  print(age >> 20);
}
